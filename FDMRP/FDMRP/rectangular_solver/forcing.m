function [fmat]=forcing(xs,ys)
    N=length(xs);       
    M=length(ys);       
    fmat=zeros(N,M);   
    for j=1:1:M
        y=ys(j);
        for i=1:1:N
            x=xs(i);
            fmat(i,j)=-4*cos(2*(y-1)*(x-.5)*pi)*(y-1)^2*pi^2*(3*exp(-100*(x-.95)^2-10*(y-1)^2)+1)-12*sin(2*(y-1)*(x-.5)*pi)*(y-1)*pi*(-200*x+190.00)*exp(-100*(x-.95)^2-10*(y-1)^2)-660*cos(2*(y-1)*(x-.5)*pi)*exp(-100*(x-.95)^2-10*(y-1)^2)+3*cos(2*(y-1)*(x-.5)*pi)*(-200*x+190.00)^2*exp(-100*(x-.95)^2-10*(y-1)^2)-4*cos(2*(y-1)*(x-.5)*pi)*(x-.5)^2*pi^2*(3*exp(-100*(x-.95)^2-10*(y-1)^2)+1)-12*sin(2*(y-1)*(x-.5)*pi)*(x-.5)*pi*(-20*y+20)*exp(-100*(x-.95)^2-10*(y-1)^2)+3*cos(2*(y-1)*(x-.5)*pi)*(-20*y+20)^2*exp(-100*(x-.95)^2-10*(y-1)^2);
        end
    end
end
